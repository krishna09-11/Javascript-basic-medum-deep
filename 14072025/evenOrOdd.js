function EvenOrOdd(num){
    if (num % 2 == 0 ){
        return true;
    }
    else {
        return false;
    }
}


let x = 10 ;
let y = 20 ;
let z = 45;

console.log("X is Even ", EvenOrOdd(x));

console.log("Y is Even ", EvenOrOdd(y));

console.log("Z is Even ", EvenOrOdd(z));

